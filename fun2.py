def greet():
    """
    A simple function with no parameters and no return type.
    Just prints a greeting message.
    """
    print("Hello! Welcome to Python programming")
    print("This function has no parameters")
    print("And doesn't return anything")

def show_info():
    """
    Another function with no parameters and no return type.
    Displays some basic information.
    """
    print("Current task: Learning about functions")
    print("Topic: Functions without parameters and return values")
    print("These functions just perform actions!")

# Calling the functions
print("Calling first function:")
greet()

print("\nCalling second function:")
show_info()